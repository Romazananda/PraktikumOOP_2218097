    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        // TODO add your handling code here:
        CetakLaptop.setText("");
        Gadget_Laptop Laptop = new Gadget_Laptop();
        Laptop.dataMerk(txtMerk.getText());
        Laptop.dataWarna(txtWarna.getText());
        Laptop.dataProcessor(txtProcessor.getText());
        Laptop.dataLayar(txtLayar.getText());

        CetakLaptop.append("Harga Dan Spesifikasi Laptop\n");
        CetakLaptop.append("-----------------------------------\n");
        CetakLaptop.append("Merk            : "+ Laptop.cetakMerk()+"\n");
        CetakLaptop.append("Layar            : "+ Laptop.cetakLayar()+"\n");
        CetakLaptop.append("Warna         : "+ Laptop.cetakWarna()+"\n");
        CetakLaptop.append("Processor  : "+ Laptop.cetakProcessor()+"\n");
        CetakLaptop.append("Ram            : "+ Laptop.cetakRam()+" GB\n");
        CetakLaptop.append("Storage       : "+ Laptop.cetakStorage()+" GB\n");
        CetakLaptop.append("Baterai       : "+ Laptop.cetakBaterai()+" WHrs\n");
        CetakLaptop.append("Harga          : Rp."+ Laptop.cetakHarga()+"\n");
    }//GEN-LAST:event_btnCetakActionPerformed

